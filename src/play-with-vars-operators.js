i += 2; // i =i +2
console.log(i); // Outputs: 1 (i remains unchanged)
i++; // i = i + 1
console.log(i); // Outputs: 2 (i is incremented by 1)

let j = 2;
j *=2; // j = j * 2
console.log(j); // Outputs: 2 (j is multiplied by 2)
j--; // j = j - 1
console.log(j); // Outputs: 3 (j is incremented by 1)

